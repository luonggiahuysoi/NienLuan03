package com.example.appbanhang.model;

public class GioHang {
    int idsp;
    String tensp;
    long giasp;
    String hinhsp;
    int solvong;

    public int getIdsp() {
        return idsp;
    }

    public void setIdsp(int idsp) {
        this.idsp = idsp;
    }

    public String getTensp() {
        return tensp;
    }

    public void setTensp(String tensp) {
        this.tensp = tensp;
    }

    public long getGiasp() {
        return giasp;
    }

    public void setGiasp(long giasp) {
        this.giasp = giasp;
    }

    public String getHinhsp() {
        return hinhsp;
    }

    public void setHinhsp(String hinhsp) {
        this.hinhsp = hinhsp;
    }

    public int getSolvong() {
        return solvong;
    }

    public void setSolvong(int solvong) {
        this.solvong = solvong;
    }
}
